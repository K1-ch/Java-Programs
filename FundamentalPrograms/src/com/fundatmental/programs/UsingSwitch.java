package com.fundatmental.programs;
import java.util.Scanner;
public class UsingSwitch {
	public static void main(String[] args) {
		 int n;
		 final int  y=90;
		Scanner sc=new Scanner(System.in);
		n=sc.nextInt();
		switch(n) {
				case 1:
		case 2:
			System.out.println("Q 4");
		case 3:
			System.out.println("Q 4");
			break;
		case y:
		case 5:
		case 6:
			System.out.println("Q 5");
			default:
				System.out.println("Q 4");
				}
		
	     	}

}
